export function getPrimaryBranch(route) {
  const firstChoice = route?.[0];
  if (!firstChoice) return "practice";
  return gameData.rounds[0].options[firstChoice]?.branchId || "practice";
}

export function getRoundScene(round, route) {
  const branch = getPrimaryBranch(route);
  if (round.sceneByBranch && round.sceneByBranch[branch]) {
    return round.sceneByBranch[branch];
  }
  return round.scene;
}

export function getRouteDetails(route) {
  return route.map((choice, index) => {
    const round = gameData.rounds[index];
    const option = round?.options?.[choice];
    return {
      roundTitle: round?.title || `第 ${index + 1} 幕`,
      choice,
      ...option
    };
  });
}
